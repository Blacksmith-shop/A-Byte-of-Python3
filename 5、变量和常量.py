i = 5   #用=给变量 i 赋值 5 。这一行叫做一个语句，语句声明需要做某件事情，在这个地方我们把变量名 i 与值 5 连接在一起.
print(i)    #接下来，我们用 print 语句打印 i 的值,就是把变量的值打印在屏幕上.
i = i + 1   #然后我们对 i 中存储的值加 1 ，再把它存回 i 。
print(i)    #我们打印它时，得到期望的值 6 。
s = '''这是一个多行的字符串.
这是第二行.'''   #类似地，我们把一个字面意义上的字符串赋给变量 s 然后打印它。
print(s)